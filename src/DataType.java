
public class DataType {

    public static void main(String[] args) {

// 1. String variable - stores text, such as "Hello"
        String name = ("Gautam Kumar");
        System.out.println("Name = " + name);

// 2. Int variable - store integers(whole number)
        int age = 21;
        System.out.println("Age = " + age);

// 3. float Variable - stores floating point numbers, with decimals, such as 19.99 or -19.99
        float birth = 10.10f;
        System.out.println("D.O.M = " + birth);

// 4. Char Variable - stores single characters, such as 'a' or 'B'
        char bloodgroup = ('B');
        System.out.println("Blood Group = " + bloodgroup);

// boolean Variable -  stores values with two states: true or false
        boolean college =  (true);
        System.out.println("Are you prusing B.tech = " + college);
    }

}
