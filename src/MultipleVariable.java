public class MultipleVariable {
    public static void main(String[] args) {

//  1. To combine both text and a variable, use the + character  (concatenation)
        String name = "Gautam";
        System.out.println(  " Hello " + name); 

        String Firstname = "Gautam";
        String Lastname = "Kumar";
        System.out.println(Firstname  + " " + Lastname );

// 2. In Java, the + symbol For numbers, it adds values together.
        int a = 10;
        int b = 5;
        int sum = a+b;
        System.out.println("Sum of two no. = " + sum);

// 3. MultipleVariable
        int x = 6 , y = 3 ,  z = 19;
        int Add  = x+y+z;
        System.out.println("Add = " + Add );


        
    }

}
